# ── POST /analyze ─────────────────────────────────────────────────────────────

@app.post(
    "/analyze",
    response_model = AnalyzeResponse,
    summary        = "Analyze a clinical note",
    tags           = ["Inference"],
)
def analyze(request: AnalyzeRequest):
    """
    ## Full VitaTwin Analysis Pipeline

    Given a clinical note text, this endpoint runs:

    1. **BERT Prediction** — Hierarchical Bio_ClinicalBERT classifies the note
       into a broad medical group (Level 1) and exact specialty (Level 2)

    2. **Explainable AI** — LIME token importance + attention weight extraction
       identify which clinical terms drove the prediction

    3. **Insight Generation** — Risk scoring, red-flag signal detection,
       and recommended clinical actions

    ### Returns
    - Predicted specialty + confidence
    - Top influential clinical terms (LIME + Attention merged)
    - Risk level (Low / Moderate / High / Critical)
    - Detected clinical signals (e.g. "chest pain", "elevated troponin")
    - Recommended actions and monitoring parameters
    """
    if not EXPLAIN_AVAILABLE:
        raise HTTPException(
            status_code = 503,
            detail      = "Explain module unavailable. Check model/explain.py and saved model.",
        )
    if not INSIGHTS_AVAILABLE:
        raise HTTPException(
            status_code = 503,
            detail      = "Insights module unavailable. Check model/insights.py.",
        )

    t0        = time.time()
    sample_id = request.sample_id or f"api_{int(t0)}"

    try:
        # ── Step 1+2: Explain (prediction + XAI) ─────────────────────────────
        explain_report = run_explain(
            request.text,
            sample_id    = sample_id,
            save_plots   = request.save_plots,
            lime_samples = request.lime_samples,
        )

        # ── Step 3: Insight ───────────────────────────────────────────────────
        insight = generate_insight(explain_report)

    except FileNotFoundError as e:
        raise HTTPException(
            status_code = 503,
            detail      = f"Model not found: {e}. Run train.py first.",
        )
    except Exception as e:
        raise HTTPException(
            status_code = 500,
            detail      = f"Analysis failed: {e}",
        )

    processing_time = round(time.time() - t0, 3)

    return AnalyzeResponse(
        sample_id             = sample_id,
        predicted_group       = explain_report["predicted_group"],
        group_confidence      = explain_report["group_confidence"],
        predicted_specialty   = explain_report["predicted_specialty"],
        specialty_confidence  = explain_report["specialty_confidence"],
        lime_tokens           = explain_report.get("lime_tokens", {}),
        attention_tokens      = [
            AttentionToken(**t)
            for t in explain_report.get("attention_tokens", [])
        ],
        top_influential_terms = explain_report.get("top_influential_terms", []),
        risk_level            = insight["risk_level"],
        risk_score            = insight["risk_score"],
        risk_emoji            = insight["risk_emoji"],
        detected_signals      = [
            DetectedSignal(**s) for s in insight.get("detected_signals", [])
        ],
        summary               = insight["summary"],
        recommended_actions   = insight["recommended_actions"],
        monitoring_parameters = insight["monitoring_parameters"],
        confidence_note       = insight["confidence_note"],
        processing_time_sec   = processing_time,
    )


# ── POST /ask ─────────────────────────────────────────────────────────────────

@app.post(
    "/ask",
    response_model = AskResponse,
    summary        = "Ask a clinical question using RAG",
    tags           = ["RAG"],
)
def ask(request: AskRequest):
    """
    ## RAG (Retrieval-Augmented Generation) Pipeline

    Given a clinical query (and optionally a patient note), this endpoint:

    1. **Embeds** the query using Sentence Transformers (`all-MiniLM-L6-v2`)
    2. **Retrieves** the top-K most semantically similar notes from the FAISS vector store
    3. **Generates** a grounded clinical answer using Claude (Anthropic API)

    ### Example queries
    - *"What is the main concern for this patient?"*
    - *"Which patients show signs of cardiac risk?"*
    - *"What follow-up is recommended for this presentation?"*

    ### Note
    - The RAG index must be built first: `python rag/rag.py --build --csv data/notes.csv`
    - Set `ANTHROPIC_API_KEY` environment variable or pass `api_key` in the request body
    - Set `retrieve_only: true` to skip LLM generation and just return similar notes
    """
    if not RAG_AVAILABLE:
        raise HTTPException(
            status_code = 503,
            detail      = "RAG module unavailable. Check rag/rag.py.",
        )

    # Check index exists
    rag_index_path = os.path.join(PROJECT_ROOT, "rag", "store", "index.faiss")
    if not os.path.isfile(rag_index_path):
        raise HTTPException(
            status_code = 503,
            detail      = (
                "RAG vector index not found. "
                "Build it first: python rag/rag.py --build --csv data/notes.csv"
            ),
        )

    t0 = time.time()

    try:
        result = rag_ask(
            query         = request.query,
            note_context  = request.note,
            top_k         = request.top_k,
            retrieve_only = request.retrieve_only,
            api_key       = request.api_key,
        )
    except Exception as e:
        raise HTTPException(
            status_code = 500,
            detail      = f"RAG pipeline failed: {e}",
        )

    processing_time = round(time.time() - t0, 3)

    retrieved_notes = [
        RetrievedNote(
            idx           = r.get("idx", 0),
            specialty     = r.get("specialty", ""),
            description   = r.get("description", ""),
            transcription = r.get("transcription", ""),
            sample_name   = r.get("sample_name", ""),
            score         = r.get("score", 0.0),
        )
        for r in result.get("retrieved", [])
    ]

    return AskResponse(
        query               = result["query"],
        note_context        = result.get("note_context", ""),
        retrieved           = retrieved_notes,
        answer              = result.get("answer", ""),
        processing_time_sec = processing_time,
    )


# ── POST /analyze-and-ask  (bonus: combined pipeline) ─────────────────────────

class AnalyzeAndAskRequest(BaseModel):
    text: str = Field(
        ...,
        min_length  = 10,
        description = "Clinical note text",
        example     = (
            "Patient is a 58-year-old male with chest pain, "
            "dyspnea, and elevated troponin."
        ),
    )
    query: Optional[str] = Field(
        default     = "What is the main concern for this patient and what actions are recommended?",
        description = "Question to answer via RAG after analyzing the note",
    )
    lime_samples : Optional[int]  = Field(default=100, ge=50, le=500)
    save_plots   : Optional[bool] = Field(default=False)
    retrieve_only: Optional[bool] = Field(default=False)
    api_key      : Optional[str]  = Field(default=None)


@app.post(
    "/analyze-and-ask",
    summary = "Full pipeline: analyze note + RAG answer",
    tags    = ["Inference"],
)
def analyze_and_ask(request: AnalyzeAndAskRequest):
    """
    ## Combined Endpoint

    Runs the full VitaTwin pipeline in one call:
    1. BERT prediction + XAI + Insight (from `/analyze`)
    2. RAG retrieval + Claude answer (from `/ask`) — using the note text as context

    Ideal for the frontend demo where both results are needed together.
    """
    t0        = time.time()
    sample_id = f"combined_{int(t0)}"

    # ── Analyze ────────────────────────────────────────────────────────────
    analyze_result = None
    if EXPLAIN_AVAILABLE and INSIGHTS_AVAILABLE:
        try:
            explain_report = run_explain(
                request.text,
                sample_id    = sample_id,
                save_plots   = request.save_plots,
                lime_samples = request.lime_samples,
            )
            insight = generate_insight(explain_report)
            analyze_result = {
                "predicted_group"      : explain_report["predicted_group"],
                "group_confidence"     : explain_report["group_confidence"],
                "predicted_specialty"  : explain_report["predicted_specialty"],
                "specialty_confidence" : explain_report["specialty_confidence"],
                "top_influential_terms": explain_report["top_influential_terms"],
                "lime_tokens"          : explain_report.get("lime_tokens", {}),
                "risk_level"           : insight["risk_level"],
                "risk_score"           : insight["risk_score"],
                "risk_emoji"           : insight["risk_emoji"],
                "detected_signals"     : insight["detected_signals"],
                "summary"              : insight["summary"],
                "recommended_actions"  : insight["recommended_actions"],
                "monitoring_parameters": insight["monitoring_parameters"],
                "confidence_note"      : insight["confidence_note"],
            }
        except Exception as e:
            analyze_result = {"error": str(e)}

    # ── RAG Ask ────────────────────────────────────────────────────────────
    rag_result = None
    rag_index_path = os.path.join(PROJECT_ROOT, "rag", "store", "index.faiss")
    if RAG_AVAILABLE and os.path.isfile(rag_index_path):
        try:
            rag_result = rag_ask(
                query         = request.query,
                note_context  = request.text,
                top_k         = 5,
                retrieve_only = request.retrieve_only,
                api_key       = request.api_key,
            )
        except Exception as e:
            rag_result = {"error": str(e)}

    return {
        "sample_id"         : sample_id,
        "analysis"          : analyze_result,
        "rag"               : rag_result,
        "processing_time_sec": round(time.time() - t0, 3),
    }


