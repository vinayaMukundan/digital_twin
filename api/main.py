# ── /analyze ──────────────────────────────────────────────────────────────────

class AnalyzeRequest(BaseModel):
    text: str = Field(
        ...,
        min_length  = 10,
        description = "Clinical note or patient transcription text to analyze",
        example     = (
            "Patient is a 58-year-old male presenting with chest pain, "
            "dyspnea on exertion, and elevated troponin levels. "
            "History of hypertension and type 2 diabetes."
        ),
    )
    sample_id: Optional[str] = Field(
        default     = None,
        description = "Optional identifier for this sample (used in saved output filenames)",
        example     = "patient_001",
    )
    save_plots: Optional[bool] = Field(
        default     = False,
        description = "If true, save LIME and attention PNG plots to model/explanations/",
    )
    lime_samples: Optional[int] = Field(
        default     = 150,
        ge          = 50,
        le          = 500,
        description = "Number of LIME perturbation samples (higher = more accurate, slower)",
    )


class DetectedSignal(BaseModel):
    term   : str
    weight : int
    label  : str


class AttentionToken(BaseModel):
    token           : str
    attention_score : float


class AnalyzeResponse(BaseModel):
    sample_id              : str
    # Prediction
    predicted_group        : str
    group_confidence       : float
    predicted_specialty    : str
    specialty_confidence   : float
    # XAI
    lime_tokens            : dict
    attention_tokens       : List[AttentionToken]
    top_influential_terms  : List[str]
    # Insight
    risk_level             : str
    risk_score             : int
    risk_emoji             : str
    detected_signals       : List[DetectedSignal]
    summary                : str
    recommended_actions    : List[str]
    monitoring_parameters  : List[str]
    confidence_note        : str
    # Meta
    processing_time_sec    : float


# ── /ask ──────────────────────────────────────────────────────────────────────

class AskRequest(BaseModel):
    query: str = Field(
        ...,
        min_length  = 3,
        description = "Clinical question to answer using the RAG pipeline",
        example     = "What is the main concern for this patient?",
    )
    note: Optional[str] = Field(
        default     = None,
        description = "Patient note to ground the query (optional)",
        example     = (
            "Patient has chest pain, dyspnea, and elevated troponin."
        ),
    )
    top_k: Optional[int] = Field(
        default     = 5,
        ge          = 1,
        le          = 20,
        description = "Number of similar notes to retrieve from the vector store",
    )
    retrieve_only: Optional[bool] = Field(
        default     = False,
        description = "If true, skip LLM generation and return retrieved notes only",
    )
    api_key: Optional[str] = Field(
        default     = None,
        description = (
            "Anthropic API key. If not provided, uses ANTHROPIC_API_KEY env var."
        ),
    )


class RetrievedNote(BaseModel):
    idx          : int
    specialty    : str
    description  : str
    transcription: str
    sample_name  : str
    score        : float


class AskResponse(BaseModel):
    query           : str
    note_context    : str
    retrieved       : List[RetrievedNote]
    answer          : str
    processing_time_sec : float


