@app.exception_handler(Exception)
async def global_exception_handler(request: Request, exc: Exception):
    tb = traceback.format_exc()
    return JSONResponse(
        status_code = 500,
        content     = {
            "error"     : str(exc),
            "traceback" : tb,
            "path"      : str(request.url),
        },
    )


