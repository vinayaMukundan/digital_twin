@app.get(
    "/health",
    response_model = HealthResponse,
    summary        = "Health check",
    tags           = ["System"],
)
def health_check():
    """
    Returns the health status of the API and availability of each module.
    Use this to confirm the server is running before sending inference requests.
    """
    model_path     = os.path.join(PROJECT_ROOT, "model", "saved_model", "level1")
    rag_index_path = os.path.join(PROJECT_ROOT, "rag", "store", "index.faiss")

    model_exists     = os.path.isdir(model_path)
    rag_index_exists = os.path.isfile(rag_index_path)

    all_ok = EXPLAIN_AVAILABLE and INSIGHTS_AVAILABLE and model_exists

    return HealthResponse(
        status          = "ok" if all_ok else "degraded",
        explain_module  = EXPLAIN_AVAILABLE,
        insights_module = INSIGHTS_AVAILABLE,
        rag_module      = RAG_AVAILABLE,
        rag_index_built = rag_index_exists,
        model_path      = model_path,
        message         = (
            "All systems operational."
            if all_ok
            else "Some modules unavailable — check logs."
        ),
    )


